    <h2>DST - DOENÇAS SEXUALMENTE TRANSMISSÍVEIS</h2>
    <p align="justify">As doenças sexualmente transmissíveis (DST), conhecidas por doenças venéreas, 
    são transmitidas essencialmente pelo contato direto, mantido através de relações sexuais onde o parceiro ou parceira necessariamente porta a doença, 
    e indireto por meio de compartilhamento de utensílios pessoais mal higienizados (roupas íntimas), ou manipulação indevida de objetos contaminados (lâminas e seringas).
    Os principais agentes patogênicos são os vírus, as bactérias e os fungos. Essas doenças acometem principalmente o público jovem, tanto de países em desenvolvimento como industrializados, 
    nseqüência de vários fatores de relevância familiar e governamental: a promiscuidade (descuido) individual com a saúde e a carência ou mesmo a falta de programas educativos.
    De modo geral, o uso de preservativo, associado a alguns cuidados, impedem o contágio e disseminação. Contudo se não forem diagnosticadas e tratadas corretamente, 
    além do processo infeccioso, podem levar à infertilidade, gravidez, surgimento de outras doenças oportunistas e até a morte.</p>
    <h3>Algumas das principais DST's:</h3>
    <p align="justify"><b>Sífilis</b> - Transmitida pela bactéria Treponema pallidum, é uma doença com evolução crônica (lenta) com surgimento de um cancro duro (lesão) 
    nos órgãos genitais e posterior aparecimento de lesões espalhadas pelo corpo. Quando generalizada, causa complicações cardiovasculares e nervosas, 
    desencadeando nas mulheres o aborto ou o parto prematuro.
    <br>
    <b>Gonorréia</b> - O contágio pela bactéria Neisseria gonorrheae, provoca a inflamação da uretra (canal urinário), 
    pode alastrar-se para outros órgãos causando complicações como: artrite, meningite e problemas cardíacos.
    <br>
    <b>Tricomona</b> – Causada pelo protozoário do gênero Trichomonas Donne, atinge, principalmente, 
    o aparelho digestivo e genital, causando inflamação do canal vaginal, nas mulheres, e da uretra nos homens.
    <br>
    <b>Clamídia</b> - O contágio pela bactéria Chlamydia trachomatis provoca inflamação dos canais genitais e urinários. 
    Nas mulheres, pode ocasionar a formação de abscessos (obstruções com dilatação), infertilidade e dores pélvicas. Nos homens pode provocar esterilidade.</p>

    <h2>PRESERVATIVOS E LUBRIFICANTES</h2>
    <p align="justify">O uso da camisinha masculina ou da feminina e do gel lubrificante, continua sendo uma importante estratégia para diminuição da infecção pelo HIV e outras DST. 
    Procure saber em seu município onde acessar na rede pública de serviços da saúde os insumos de prevenção às DST/aids e à gravidez não planejada. É um direito seu.</p>
    
    <h3>PRESERVATIVO MASCULINO</h3>

    <p align="justify">O preservativo masculino, se utilizado corretamente em todas as relações sexuais, protege contra o HIV, 
    contra a maioria das DST e protege também da gravidez não planejada. Coloque você mesmo a camisinha no seu parceiro ou parceira, 
    use seus dedos para ter prazer nesse momento, ou coloque a camisinha com a boca, faça brincadeiras eróticas para e torne a camisinha parte da relação de uma forma prazerosa. 
    Usar a camisinha junto com o gel lubrificante deixa o sexo mais seguro e mais gostoso.
    Sempre verifique a data de validade e não deixe no bolso, porta luvas do carro ou amassado em mochilas.</p>
    
    <h3>PRESERVATIVO FEMININO</h3>

    <p align="justify">O preservativo feminino, assim como o masculino, pode ser utilizado como dupla proteção quando o assunto é gravidez não planejada e prevenção contra DST.
    Uma vantagem é que a camisinha feminina pode ser usada por quem é alérgico ao látex. 
    Outra vantagem é que não há necessidade de colocar a camisinha somente na hora do sexo. Ela pode ser colocada até oito horas antes e não precisa ser retirada logo após a ejaculação, 
    pois não depende da ereção do pênis para manter-se no lugar e evitar o vazamento do esperma. 
    Quando a camisinha feminina é usada corretamente, a chance de falha é muito pequena. Sempre verifique a data de validade e não deixe no bolso, 
    porta luvas do carro ou amassada na bolsa.
    </p>

    <h3>GEL LUBRIFICANTE</h3>

    <p align="justify">O gel lubrificante, a base de água, pode ser usado com ou sem preservativo. Mesmo quando usado sem a camisinha, 
    o gel diminui a chance de pequenos ferimentos no ânus e na vagina, dando alguma proteção.  
    Nunca use vaselina, hidratantes, óleos e margarina, pois podem danificar o preservativo ou mesmo causar alergias e irritações na pele.
    Sempre verifique a data de validade do produto. 
    O gel está disponível nos Serviços de Assistência Especializada às DST/AIDS. É distribuído especialmente para travestis, gays, 
    homens que fazem sexo com homens, pessoas que exercem a prostituição, idosos e pessoas que vivem com HIV. </p>


