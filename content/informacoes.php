    <h2>AIDS E JOVENS</h2>
    <p align="justify">No Estado de São Paulo, de 1980 até junho de 2013, mais de 200.000 pessoas tiveram AIDS. 
    Destas pessoas 24.000 tinham entre 13 e 24 anos, isto significa que mais ou menos 10% dos 
    casos existentes eram em jovens.
    É importante lembrar que esses indivíduos já tem a doença AIDS, que é diferente de estar 
    infectado pelo vírus, mas não apresentar a doença. As pessoas podem ficar mais ou menos de 
    7 a 10 anos com o vírus do HIV no sangue mas sem adoecer. Isso nos mostra que quem está 
    adoecendida 29 anos se infectou na adolescência, provavelmente em torno dos 15 aos 19 anos.</p>

