		 <p align="justify">PROGRAMA MUNICIPAL DST/AIDS</b></p>

    <p align="justify">A Vigilância Epidemiológica tem como atribuição prever qualquer mudança nos
     fatores condicionantes de saúde individual e coletiva, com objetivo de recomendar e adotar as medidas de controle. 
     Portanto, é importante a atualização das normas de Vigilância Epidemiológica, tendo em vista o desenvolvimento científico, 
     a introdução de novas ferramentas laboratoriais, monitoramento e avaliação das medidas de prevenção adotadas e o impacto nos indicadores de saúde.
	 Com o objetivo de informar e diminuir a vulnerabilidade de adolescentes e jovens às DST, à infecção pelo HIV e à gravidez não planejada, 
	 a Secretaria Municipal de Saúde através do departamento de Vigilância Epidemiológica, Programa Municipal DST/AIDS, elaborou esse projeto com intuito 
	 de formar parceria com a Secretaria Municipal de Educação, CRAS, CREAS, Casa do Adolescente I, II e III, Projeto Ações Preventivas na Escola (APE), 
	 relacionada ao Eixo Saúde do Programa Escola da Família da Secretaria de Estado da Educação, corpo docente e discente das Escolas Estaduais e Municipais. 
	 O projeto FAM - Formação Agentes Multiplicadores, irá realizar capacitações para profissionais de diversas áreas, adolescentes e jovens. 
	 Na prática, entretanto, não é raro se escutar que a abordagem dos temas relacionados à sexualidade e saúde reprodutiva para adolescentes 
	 “poderia levar meninas e meninos a iniciarem mais cedo sua vida sexual”.
	 A resposta às situações como estas podem ser ancoradas em marcos legais – nacionais e internacionais – 
	 que garantem o direito da população adolescente e jovem à saúde e à vida.
